  private TrieNode suffixTree = new TrieNode();

  public static final char ENDING_CHARACTER = '$';

  // @param dict A dictionary of words dict
  public Typeahead(Set<String> dict) {
    // do initialize if necessary
    for(String str : dict) {
      for(int i = 0; i < str.length(); i++) {
        addStringToSuffixTree(str, i);
      }
    }
  }

  // @param str: a string
  // @return a list of words
  public List<String> search(String str) {
    // Write your code here

    TrieNode currentNode = suffixTree;

    for(int i = 0; i < str.length(); i++) {
      char ch = str.charAt(i);
      TrieNode childNode = currentNode.children.get(ch);
      if(childNode == null) {
        return Collections.EMPTY_LIST;
      }
      currentNode = childNode;
    }
    Set<String> results = new HashSet<>();
    search(currentNode,results);
    return new ArrayList<>(results);
  }

  // @param str: a string
  // @return a list of words
  public void search(TrieNode parent, Set<String> results) {
    // Write your code here
    for(Entry<Character, TrieNode> entry : parent.children.entrySet()) {
      Character ch = entry.getKey();
      if (ch.equals(ENDING_CHARACTER)) {
        results.addAll(entry.getValue().originalString);
      }else {
        search(entry.getValue(),results);
      }
    }
  }
  private void addStringToSuffixTree(String str, int index) {
    addStringToSuffixTree(suffixTree, str, index);
  }


  private void addStringToSuffixTree(TrieNode parent, String str, int index) {

    if (index == str.length()) {
      TrieNode tmpNode = parent.children.get(ENDING_CHARACTER);
      if (tmpNode == null) {
        tmpNode = new TrieNode();
      }
      if(tmpNode.originalString == null) {
        tmpNode.originalString = new ArrayList<>();
      }
      tmpNode.originalString.add(str);

      parent.children.put(ENDING_CHARACTER, tmpNode);
      return;
    }
    char ch = str.charAt(index);
    TrieNode tmpNode = parent.children.get(ch);
    if(tmpNode == null) {
      tmpNode = new TrieNode();
    }
    parent.children.put(ch, tmpNode);
    addStringToSuffixTree(tmpNode, str, index + 1);
  }

